//converts string into integer
console.log(parseInt("45"));
console.log(parseInt("45ppppppppppppooooooo"));
console.log(parseInt("5.79"));

//output:45, 45, 5

//parseFloat === prints string which is float as float
console.log(parseFloat("45"));
console.log(parseFloat("45ppppppppppppooooooo"));
console.log(parseFloat("5.79"));

//output:45, 45, 5.79